/**
 * Resolve a dot-notation key from a nested object.
 * e.g. resolve(obj, 'redflags.offPlatform.title')
 */
function resolve(obj, key) {
  return key.split('.').reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), obj);
}

/**
 * Translate a key with optional parameter interpolation.
 * @param {string} key  Dot-notation key, e.g. 'ui.skillMatchPercent'
 * @param {Object} [params]  Interpolation values, e.g. { percent: 85 }
 * @returns {string} Translated string (falls back to English, then returns the key itself)
 */
export function t(key, params) {
  let value = resolve(translations[currentLanguage], key)
    ?? resolve(translations.en, key)
    ?? key;

  if (params && typeof value === 'string') {
    value = value.replace(/\{\{(\w+)\}\}/g, (_, p) =>
      params[p] !== undefined ? params[p] : `{{${p}}}`
    );
  }

  return value;
}

/**
 * Set the active language.
 * @param {string} langCode  One of the keys in LANGUAGES
 */
export function setLanguage(langCode) {
  if (translations[langCode]) {
    currentLanguage = langCode;
  }
}

/**
 * Get the current language code.
 * @returns {string}
 */
export function getLanguage() {
  return currentLanguage;
}
